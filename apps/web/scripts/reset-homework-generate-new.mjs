// ─── Вспомогательные функции для создания сдач ──────────────────────────────

function spreadCreatedAt(dueDateIso) {
  // created_at — за 2-6 дней до due_date, но не раньше 17 июля (нижняя граница диапазона).
  const due = new Date(dueDateIso).getTime();
  const lowerBound = new Date("2026-07-17T00:00:00+05:00").getTime();
  const daysBefore = 2 + Math.random() * 4;
  const created = Math.max(lowerBound, due - daysBefore * 86400000);
  return new Date(created).toISOString();
}

