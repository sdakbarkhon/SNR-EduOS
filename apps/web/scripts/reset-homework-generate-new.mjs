function tierForGrade(grade) {
  if (grade === 5) return "strong";
  if (grade === 4) return "good";
  if (grade === 3) return "average";
  return "weak";
}

function pick(list) {
  return list[Math.floor(Math.random() * list.length)];
}

// Точное распределение 5≈30% / 4≈40% / 3≈25% / 2≈5% (веса, не приближение через padding).
function rollGrade() {
  const r = Math.random();
  if (r < 0.30) return 5;
  if (r < 0.70) return 4; // 0.30 + 0.40
  if (r < 0.95) return 3; // 0.70 + 0.25
  return 2; // остаток 0.05
}

function commentFor(grade) {
  return pick(COMMENTS_BY_TIER[tierForGrade(grade)]);
}

// Разбрасывает N дат по диапазону [from, to] почти равномерно + небольшой джиттер.
function spreadDates(n, fromIso, toIso) {
  const from = new Date(fromIso).getTime();
  const to = new Date(toIso).getTime();
  const span = to - from;
  const out = [];
  for (let i = 0; i < n; i++) {
    const base = from + (span * (i + 0.5)) / n;
    const jitter = (Math.random() - 0.5) * (span / n) * 0.6;
    out.push(new Date(Math.min(to, Math.max(from, base + jitter))).toISOString());
  }
  return out;
}

function daysAfter(iso, days) {
  return new Date(new Date(iso).getTime() + days * 86400000 + Math.random() * 3 * 3600000).toISOString();
}

